<script>
    // Календарь с праздниками
    const holidays = {
        '01-01': 'Новогодние каникулы',
        '01-02': 'Новогодние каникулы',
        '01-03': 'Новогодние каникулы',
        '01-04': 'Новогодние каникулы',
        '01-05': 'Новогодние каникулы',
        '01-06': 'Новогодние каникулы',
        '01-07': 'Рождество Христово',
        '01-08': 'Новогодние каникулы',
        '02-23': 'День защитника Отечества',
        '03-08': 'Международный женский день',
        '05-01': 'Праздник Весны и Труда',
        '05-09': 'День Победы',
        '06-12': 'День России',
        '11-04': 'День народного единства',
    };
    let currentMonth = new Date().getMonth();
    let currentYear = new Date().getFullYear();
    function generateCalendar(month = currentMonth, year = currentYear) {
        const months = ['Январь', 'Февраль', 'Март', 'Апрель', 'Май', 'Июнь', 'Июль', 'Август', 'Сентябрь', 'Октябрь', 'Ноябрь', 'Декабрь'];
        const daysInMonth = new Date(year, month + 1, 0).getDate();
        const firstDay = new Date(year, month, 1).getDay();
        let start = firstDay === 0 ? 6 : firstDay - 1;
        const today = new Date();
        const isCurrentMonth = (month === today.getMonth() && year === today.getFullYear());
        let html = '<div class="calendar-header">';
        html += `<button class="calendar-prev" onclick="changeMonth(-1)">&#10094;</button>`;
        html += `<h4>${months[month]} ${year}</h4>`;
        html += `<button class="calendar-next" onclick="changeMonth(1)">&#10095;</button>`;
        html += '</div>';
        html += '<table class="calendar-table"><thead><tr><th>Пн</th><th>Вт</th><th>Ср</th><th>Чт</th><th>Пт</th><th>Сб</th><th>Вс</th></tr></thead><tbody><tr>';
        for (let i = 0; i < start; i++) html += '<td class="empty-cell"></td>';
        for (let day = 1; day <= daysInMonth; day++) {
            if ((start + day - 1) % 7 === 0 && day > 1) html += '</tr><tr>';
            const isToday = (isCurrentMonth && day === today.getDate()) ? ' today' : '';
            const dateKey = `${String(month+1).padStart(2,'0')}-${String(day).padStart(2,'0')}`;
            const isHoliday = holidays[dateKey] ? ' holiday' : '';
            html += `<td class="day-cell${isToday}${isHoliday}">${day}</td>`;
        }
        const totalCells = start + daysInMonth;
        const remaining = 7 - (totalCells % 7);
        if (remaining < 7) for (let i = 0; i < remaining; i++) html += '<td class="empty-cell"></td>';
        html += '</tr></tbody></table>';
        document.getElementById('calendar').innerHTML = html;
    }
    function changeMonth(direction) {
        currentMonth += direction;
        if (currentMonth > 11) { currentMonth = 0; currentYear++; }
        else if (currentMonth < 0) { currentMonth = 11; currentYear--; }
        generateCalendar(currentMonth, currentYear);
    }
    window.onload = function() { generateCalendar(); };
</script>
